// Define a flag that controls debug console messages
// This flag will allow debug compiler messages to be outputed into the console
// This flag could be set to 0 (off) or 1 (on)
#define FLAG_CONSOLE_DEBUG_MESSAGES 1

// Define a flag that controls informative console messages
// This flag will allow informative compiler messages to be outputed into the console
// This flag could be set to 0 (off) or 1 (on)
#define FLAG_CONSOLE_INFO_MESSAGES 1

// Define a flag that controls warning console messages
// This flag will allow warning compiler messages to be outputed into the console
// This flag could be set to 0 (off) or 1 (on)
#define FLAG_CONSOLE_WARNING_MESSAGES 1

// Define a flag that controls error console messages
// This flag will allow error compiler messages to be outputed into the console
// This flag could be set to 0 (off) or 1 (on)
#define FLAG_CONSOLE_ERROR_MESSAGES 1

// Define a flag that controls the behaviour of the compiler after outputing error messages into
// the console
// This flag could be set to 0 (keep working after errors) or 1 (exit after errors)
#define FLAG_CONSOLE_ERROR_MESSAGES_EXIT 1
